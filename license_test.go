func TestGetContentKey(t *testing.T) {
	options := Options{
		Key:      key,
		IV:       iv,
		Provider: "widevine_test",
	}
	wv := New(options)

	contentID := "testing"
	resp := wv.GetContentKey(contentID)

	if resp.Status != "OK" {
		t.Error()
	}
}

